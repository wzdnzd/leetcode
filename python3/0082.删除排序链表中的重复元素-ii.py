# 输出：[2,3]
#
#
#
#
# 提示：
#
#
# 链表中节点数目在范围 [0, 300] 内
# -100 <= Node.val <= 100
# 题目数据保证链表已经按升序 排列
#
#
#


# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def deleteDuplicates(self, head: Optional[ListNode]) -> Optional[ListNode]:
        if not head:
            return head

        dummy = ListNode(next=head)
        pre = dummy
        while head and head.next:
            if head.val == head.next.val:
                num = head.val
                while head and head.val == num:
                    head = head.next
                pre.next = head
            else:
                pre = pre.next
                head = head.next

        return dummy.next


