// @lc code=start
package main

func lengthOfLongestSubstring(s string) int {
	if s == "" {
		return 0
	}

	lookup := make(map[byte]int)
	i, ans := 0, 0
	for j := 0; j < len(s); j++ {
		if v, ok := lookup[s[j]]; ok && v > i {
			i = v
		}

		k := j - i + 1
		if k > ans {
			ans = k
		}

		lookup[s[j]] = j + 1
	}

	return ans
}

