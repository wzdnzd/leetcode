// @lc code=start
class LRUCache {
    class DeLinkedNode {
        private int key;
        private int value;

        private DeLinkedNode pre;
        private DeLinkedNode next;

        public DeLinkedNode() {
        }

        public DeLinkedNode(int k, int v) {
            key = k;
            value = v;
        }
    }

    private int size;
    private final int capacity;
    private DeLinkedNode head, tail;
    private final Map<Integer, DeLinkedNode> cache;

    public LRUCache(int capacity) {
        this.size = 0;
        this.capacity = capacity;
        cache = new HashMap<>(capacity);
        head = new DeLinkedNode();
        tail = new DeLinkedNode();
        head.next = tail;
        tail.pre = head;
    }

    public int get(int key) {
        DeLinkedNode node = cache.get(key);
        if (node == null)
            return -1;

        moveToHead(node);
        return node.value;
    }

    public void put(int key, int value) {
        DeLinkedNode node = cache.get(key);
        if (node == null) {
            DeLinkedNode newNode = new DeLinkedNode(key, value);
            cache.put(key, newNode);
            addToHead(newNode);
            ++size;
            if (size > capacity) {
                DeLinkedNode tail = removeTail();
                cache.remove(tail.key);
                --size;
            }
        } else {
            node.value = value;
            moveToHead(node);
        }
    }

    private void addToHead(DeLinkedNode node) {
        node.pre = head;
        node.next = head.next;
        head.next.pre = node;
        head.next = node;
    }

    private void removeNode(DeLinkedNode node) {
        node.pre.next = node.next;
        node.next.pre = node.pre;
    }

    private void moveToHead(DeLinkedNode node) {
        removeNode(node);
        addToHead(node);
    }

    private DeLinkedNode removeTail() {
        DeLinkedNode res = tail.pre;
        removeNode(res);
        return res;
    }
}

